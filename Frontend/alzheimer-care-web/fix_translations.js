function walkDir(dir) {
  let results = [];
  const list = fs.readdirSync(dir);
  for (const file of list) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);
    if (stat && stat.isDirectory()) {
      results = results.concat(walkDir(filePath));
    } else if (filePath.endsWith('.component.ts')) {
      results.push(filePath);
    }
  }
  return results;
}

// Get files from stock-admin and stock-front
const stockAdminPath = path.join(basePath, 'admin', 'stock-admin');
const stockFrontPath = path.join(basePath, 'stock-front');

let files = [];
if (fs.existsSync(stockAdminPath)) files = files.concat(walkDir(stockAdminPath));
if (fs.existsSync(stockFrontPath)) files = files.concat(walkDir(stockFrontPath));

console.log(`Found ${files.length} component files`);

// Sort keys by length descending to replace longer keys first
const sortedKeys = Object.keys(replacements).sort((a, b) => b.length - a.length);

let totalReplacements = 0;

for (const file of files) {
  let content = fs.readFileSync(file, 'utf8');
  let modified = false;

  for (const key of sortedKeys) {
    const value = replacements[key];
    // Use a regex to match the key as a standalone token (not inside another word)
    // But be careful - these keys appear standalone in templates
    if (content.includes(key)) {
      content = content.split(key).join(value);
      modified = true;
      totalReplacements++;
    }
  }

  if (modified) {
    fs.writeFileSync(file, content, 'utf8');
    console.log('Updated:', file);
  }
}

console.log(`\nTotal replacements made: ${totalReplacements}`);
